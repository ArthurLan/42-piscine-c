int		ft_bouton(int i, int j, int k)
{
	if ((i <= k && i >= j) || (i <= j && i >= k))
		return (i);
	else if ((j <= k && j >= i) || (j <= i && j >= k))
		return (j);
	else if ((k <= i && k >= j) || (k <= j && k >= i))
		return (k);
	return (0);
}
