int		ft_str_is_lowercase(char *str)
{
	int		i;
	char	cur;

	i = 0;
	while (str[i] != '\0')
	{
		cur = str[i];
		if (!(cur >= 'a' && cur <= 'z'))
			return (0);
		i++;
	}
	return (1);
}
